#include "3-calc.h"

/**
  * op_add - returns the sum of a and b
  * op_sub - return the difference of a and b
  * @a: first int
  * @b: second int
  * Return: the operation
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - the function
  * @a: the int
  * @b: the int
  * Return: the operation
  */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
  * op_mul - the function
  * @a: the int
  * @b: the int
  * Return: the operation
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - the function
  * @a: the int
  * @b: the int
  * Return: the operation
  */



int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - the function
  * @a: the int
  * @b: the int
  * Return: the operation
  */

int op_mod(int a, int b)
{
	return (a % b);
}
